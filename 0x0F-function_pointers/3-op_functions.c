#include <stdio.h>

/**
 * op_add - Function for addition
 * @a: First argument
 * @b: Second argument
 * Return: The sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function for subtraction
 * @a: First argument
 * @b: Second argument
 * Return: The difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function for multiplication
 * @a: First argument
 * @b: Second argument
 * Return: The product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function for division
 * @a: First argument
 * @b: Second argument
 * Return: The quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function for remainder
 * @a: First argument
 * @b: Second argument
 * Return: The remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
